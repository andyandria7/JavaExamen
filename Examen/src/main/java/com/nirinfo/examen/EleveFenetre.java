    int getNumberRow() {
        Statement state;
        int n = 0;
        try {
            state = con.createStatement();
            ResultSet rs = state.executeQuery("SELECT * FROM eleves");
            while (rs.next()) {
                n++;
            }

        } catch (Exception ex) {
            Logger.getLogger(EleveFenetre.class.getName()).log(Level.SEVERE, null, ex);
        }
        return n;
    }

    String[][] tabList() {
        String[][] liste = new String[this.getNumberRow()][5];
        try {
            Statement state = con.createStatement();
            ResultSet rs = state.executeQuery("SELECT * FROM eleves");
            int i = 0;
            while (rs.next()) {
                liste[i][0] = rs.getString("idEleve");
                liste[i][1] = rs.getString("nom");
                liste[i][2] = rs.getString("prenom");
                liste[i][3] = rs.getString("dateNaissance");
                liste[i][4] = rs.getString("classe");
                i++;
            }
        } catch (Exception ex) {
            Logger.getLogger(EleveFenetre.class.getName()).log(Level.SEVERE, null, ex);
        }
        return liste;
    }

