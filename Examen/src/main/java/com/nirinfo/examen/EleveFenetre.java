    public void insert(Eleve eleve) {
        try {
            PreparedStatement pst = con.prepareStatement("INSERT INTO eleves (nom, prenom, dateNaissance, classe) VALUES (?, ?, ?, ?)");
            pst.setString(1, eleve.getNom());
            pst.setString(2, eleve.getPrenom());
            Date sqlDate = Date.valueOf(eleve.getDate());
            pst.setDate(3, sqlDate);
            pst.setString(4, eleve.getClasse());
            pst.executeUpdate();
        } catch (Exception ex) {
            Logger.getLogger(EleveFenetre.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Erreur lors de l'ajout de l'élève : " + ex.getMessage());
        }
    }

    private void validerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_validerMouseClicked
        // TODO add your handling code here:
        String nom = name.getText();
        String username = first.getText();
        String classe = classes.getText();
        java.util.Date date = naissance.getDate();

        if (nom.isEmpty() || username.isEmpty() || classe.isEmpty() || date == null) {
            JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs !!");
        } else {
            try {
                Date sqlDate = new Date(date.getTime());
                Eleve eleve = new Eleve();
                eleve.setNom(nom);
                eleve.setPrenom(username);
                eleve.setDate(sqlDate.toString());
                eleve.setClasse(classe);

                this.insert(eleve);
                Afficher();
                name.setText("");
                first.setText("");
                classes.setText("");
                naissance.setDate(null);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Erreur de format de date : " + ex.getMessage());
            }
        }


    }//GEN-LAST:event_validerMouseClicked

    public Eleve getId(int id) {
        try {
            PreparedStatement pst = con.prepareCall("SELECT * FROM eleves WHERE idEleve=?");
            pst.setInt(1, id);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                return new Eleve(
                        rs.getInt("idEleve"),
                        rs.getString("nom"),
                        rs.getString("prenom"),
                        rs.getString("dateNaissance"),
                        rs.getString("classe")
                );
            }
        } catch (Exception e) {
        }
        return null;
    }

    private void infoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_infoMouseClicked
        // TODO add your handling code here:
        if (selectedId == -1) {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner une ligne dans la table.");
            return;
        }
        Eleve eleve = this.getId(selectedId);

        if (eleve != null) {
            InformationEleve info = new InformationEleve(eleve, this);
            info.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Aucune information trouvée pour cette personne.");
        }
    }//GEN-LAST:event_infoMouseClicked

